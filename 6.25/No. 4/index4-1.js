function toLowerCase(str1) { return str1.toLowerCase();}

var result1 = toLowerCase('LEEJINWOO');

function toUpperCase(str2) {return str2.toUpperCase();}

var result2 = toUpperCase('leejinwoo');

console.log(result1);

console.log(result2);


// toLowerCase는 대문자를 소문자로 바꿔주는 함수이다.
// toUpperCase는 소문자를 대문자로 바꿔주는 함수이다.